	GOTO INIT

	ORG 0X04

INTERRUPT:
	BCF INTCON, TMR0IF

TESTE0:
	BTFSS 0X30, 3
	GOTO AD1
	BTFSS 0X30, 1
	GOTO AD1
	CLRF 0X30
	INCF 0X31, F
TESTE1:
	BTFSS 0X31, 2
	GOTO FIN
	BTFSS 0X31, 1
	GOTO FIN
	CLRF 0X31
	INCF 0X32, F
TESTE2:
        BTFSS 0X32, 3
	GOTO FIN
	BTFSS 0X32, 1
	GOTO FIN
	CLRF 0X32
	INCF 0X33, F
TESTE3:
        BTFSS 0X33, 2
	GOTO FIN
	BTFSS 0X33, 1
	GOTO FIN
	CLRF 0X33
FIN:	RETFIE

AD1:
	INCF 0X30, F
	GOTO FIM
INIT:
	BCF 0X03, 6
	BSF 0x03, 5
	CLRF 0X06
	BCF 0X03, 5
	MOVLW OPTION_REG
	MOVWF FSR
	BCF 0, T0CS
	BCF 0, PSA
	BCF 0, PS2
	BSF 0, PS1
	BCF 0, PS0
	BSF INTCON, TMR0IE
	BSF INTCON, GIE
	MOVLW TRISB ;PORTB OUT
	MOVWF FSR
	CLRF 0X00
	MOVLW TRISD ;PORTD OUT
	MOVWF FSR
	CLRF 0X00
	CLRF TMR0

LOOP:
	MOVF 0x30,W
	CALL DIG
	MOVWF PORTB
	BSF PORTD, 7
	BcF PORTD, 7

	MOVF 0x31, W
	CALL DIG
	MOVWF PORTB
	BSF PORTD, 6
	BcF PORTD, 6

	MOVF 0x32, W
	CALL DIG
	MOVWF PORTB
	BSF PORTD, 5
	BcF PORTD, 5

	MOVF 0x33, W
	CALL DIG
	MOVWF PORTB
	BSF PORTD, 4
	BcF PORTD, 4


FIM: 	GOTO LOOP ; Incremento de 10 TMR0 por loop +/-



DIG: 	ADDWF PCL,F
	RETLW 0x3F ;0
	RETLW 0x06 ;1
	RETLW 0x5B ;2
	RETLW 0x4F ;3

	RETLW 0x66 ;4'
	RETLW 0x6D ;5'
	RETLW 0x7D ;6'
	RETLW 0x07 ;7'

	RETLW 0x7F ;8'
	RETLW 0x67 ;9'